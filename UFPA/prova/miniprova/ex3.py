numeros = [10, 20, 30, 40, 50, 60, 70]

#Exiba apenas os três primeiros elementos
op1 = numeros[:3]
print(op1)

#Exiba apenas os três últimos elementos
op2 = numeros[-3:]
print(op2)

#Exiba os elementos nas posições pares
op3 = numeros[::2]
print(op3)

#Inverta a lista
op4 = numeros[::-1]
print(op4)